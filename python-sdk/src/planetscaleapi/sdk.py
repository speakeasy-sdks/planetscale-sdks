
import requests
from . import utils
from .database_branch_passwords import DatabaseBranchPasswords
from .database_branches import DatabaseBranches
from .databases import Databases
from .deploy_requests import DeployRequests
from .oauth_applications import OAuthApplications
from .oauth_tokens import OAuthTokens
from .organizations import Organizations
from .users import Users
from planetscaleapi.models import shared

SERVERS = [
	"https://api.planetscale.com/v1",
]


class PlanetscaleAPI:
    
    database_branch_passwords: DatabaseBranchPasswords
    database_branches: DatabaseBranches
    databases: Databases
    deploy_requests: DeployRequests
    o_auth_applications: OAuthApplications
    o_auth_tokens: OAuthTokens
    organizations: Organizations
    users: Users

    _client: requests.Session
    _security_client: requests.Session
    _security: shared.Security
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "2.4.0"
    _gen_version: str = "1.4.5"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        
        if self._security is not None:
            self._security_client = utils.configure_security_client(self._client, self._security)
        self._init_sdks()
    

    def config_security(self, security: shared.Security):
        self._security = security
        self._security_client = utils.configure_security_client(self._client, security)
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.database_branch_passwords = DatabaseBranchPasswords(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.database_branches = DatabaseBranches(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.databases = Databases(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.deploy_requests = DeployRequests(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.o_auth_applications = OAuthApplications(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.o_auth_tokens = OAuthTokens(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.organizations = Organizations(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.users = Users(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    